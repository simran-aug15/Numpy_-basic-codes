import numpy as np
arr=np.array([10,20,30,40,50])
print("Values after their sum is",np.sum(arr))
print("Values after their mean is",np.mean(arr))
print("Values which is minimum ",np.min(arr))
print("Values which is maximum ",np.max(arr))
print("Values after their standard deviation is",np.std(arr))
print("Values after their variance  is",np.var(arr))